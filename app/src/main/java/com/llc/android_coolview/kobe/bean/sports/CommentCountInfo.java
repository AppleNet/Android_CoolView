package com.llc.android_coolview.kobe.bean.sports;

public class CommentCountInfo {

	private String qreply;
	private String total;
	private String show;
	private String comment_status;
	private String praise;
	private String dispraise;
	public String getQreply() {
		return qreply;
	}
	public void setQreply(String qreply) {
		this.qreply = qreply;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getShow() {
		return show;
	}
	public void setShow(String show) {
		this.show = show;
	}
	public String getComment_status() {
		return comment_status;
	}
	public void setComment_status(String comment_status) {
		this.comment_status = comment_status;
	}
	public String getPraise() {
		return praise;
	}
	public void setPraise(String praise) {
		this.praise = praise;
	}
	public String getDispraise() {
		return dispraise;
	}
	public void setDispraise(String dispraise) {
		this.dispraise = dispraise;
	}
	
	
}
